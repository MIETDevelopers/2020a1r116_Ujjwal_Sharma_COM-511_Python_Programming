# Built-in functions

# abs() function
x=abs(-31.2)
print(x)

# len() function
x=len("Ujjwal")
print(x)

# min() function
x=min(12,3,4,43,1)
print(x)

# max() function
x=max(12,3,4,43,1)
print(x)

# round() function
x=round(382.218223, 2)
print(x)

# isalnum() function
txt="Ujjwal25"
x=txt.isalnum()
print(x)

# type() function
a=10
print(a, "is of type", type(a))

b=10.0
print(b, "is of type", type(b))

c=1 +2j
print(c, "is of type", type(c))
